#include <stdio.h>
#include "ft_abs.h"

int main() 
{
    int a = -42;
    int b = 10;
    printf("ABS(%d) = %d\n", a, ABS(a)); // 输出: ABS(-42) = 42
    printf("ABS(%d) = %d\n", b, ABS(b)); // 输出: ABS(10) = 10
    return 0;
}

